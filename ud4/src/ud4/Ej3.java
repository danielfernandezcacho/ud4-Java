package ud4;

public class Ej3 {

	public static void main(String[] args) {
		
		int x = 10, y = 15;
		double z = 5.25, c = 22.8;
		
		System.out.println("Valor de x = " + x + " Valor de y = " + y + " Valor de n = " + z + " Valor de m = " + c);
		System.out.println("Suma X + Y = " + (x + y));
		System.out.println("Resta X - Y = " + (x - y));
		System.out.println("Producto X * Y = " + (x * y));
		System.out.println("Cociente X / Y = " + (x / y));
		System.out.println("Resto X % Y = " + (x % y));
		System.out.println("Suma N + M = " + (z + c));
		System.out.println("Resta N - M = " + (z - c));
		System.out.println("Producto N * M = " + (z * c));
		System.out.println("Cociente N / M = " + (z / c));
		System.out.println("Resto N % M = " + (z % c));
		System.out.println("El doble de X " + (x * 2));
		System.out.println("El doble de Y " + (y * 2));
		System.out.println("El doble de N " + (z * 2));
		System.out.println("El doble de M " + (c * 2));
		System.out.println("La suma de todas las variables = " + (x + y + z + c));
		System.out.println("El producto de todas las variables = " + (x * y * z * c));

	}

}
